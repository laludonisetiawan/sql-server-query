Function dan stored procedures

🍃 stored procedures adalah blok kode sql yang dapat dieksekusi dan sering digunakan untuk operasi yang
memerlukan eksekusi pernyataan sql yang kompleks atau transaksi.

🍃 Manfaat stored Procedures
    - reusabilitas: seperti fungsi, prosedur tersimpan dapat digunakan kembali dalam berbagai bagaian aplikasi.
    - keamanan: menyembunyikan logika bisnis dari pengguna akhir dan membatasi akses ke data.
    - pengurangan lalu lintas jaringan: mengurangi lalu lintas jaringan dengan mengeksekusi beberapa pernyataan sql dalam satu panggilan.
    - Kinerja: Stored procedures disimpan dalam bentuk yang telah dikompilasi di server database, yang dapat mengingkatkan kinerja
    - Transaksi: Mendukung pengelolaan transaksi yang kompleks
*/